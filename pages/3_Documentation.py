import streamlit as st
from utils.translations import get_translation

def documentation_page():
    st.title("Документация по финансовой модели")
    
    st.markdown("""
    ## Руководство по финансовой модели

    ### Параметры модели

    #### Базовые параметры
    - **Начальные пользователи**: стартовое количество пользователей в системе
    - **Конверсия в активных**: процент пользователей, совершающих транзакции (30-45%)
    - **Темпы роста**:
        - Первый год: 20% (стандартный сценарий)
        - Второй год: 10% (стандартный сценарий)
    - **Средний чек**: базовая сумма транзакции (₽3000 для стандартного сценария)

    #### Параметры программы лояльности
    - **Кэшбэк**: 15% от суммы покупки
    - **Использование баллов**: 70% от начисленных баллов используется клиентами
    - **Комиссии**:
        - Обмена: 3% при использовании баллов
        - Начисления: 5% при выдаче кэшбэка

    #### Маркетинг и операции
    - **Маркетинговые расходы**:
        - Первые 6 месяцев: фиксированный бюджет 200,000 ₽/мес
        - После 6 месяцев: 5% от выручки (стандартный сценарий)
    - **Эффективность маркетинга**: 250 новых пользователей на 100,000 ₽ (стандартный сценарий)
    - **Инфраструктура**: базовые затраты 200,000 ₽/мес с повышением при росте пользователей

    ### Сценарии и их особенности

    #### Стандартный сценарий (базовый)
    - Темпы роста: 20% и 10% (1й и 2й год)
    - Средний чек: ₽3000
    - Эффективность маркетинга: 250 пользователей на 100К ₽

    #### Пессимистичный сценарий
    - Темпы роста: 15% и 8% (на 25% ниже стандартного)
    - Средний чек: ₽2500
    - Эффективность маркетинга: 100 пользователей на 100К ₽
    - Повышенные маркетинговые расходы: 15% от выручки

    #### Оптимистичный сценарий
    - Темпы роста: 25% и 12% (на 25% выше стандартного)
    - Средний чек: ₽3500
    - Эффективность маркетинга: 300 пользователей на 100К ₽

    ### Расчетные формулы

    #### Выручка
    1. Кэшбэк = Оборот × 15%
    2. Используемые баллы = Кэшбэк × 70%
    3. Комиссионная выручка = (Используемые баллы × 3%) + (Начисленный кэшбэк × 5%)
    4. Общая выручка = Комиссии + Подписки + Реклама

    #### Расходы и прибыль
    - Ежемесячные расходы = ФОТ + Маркетинг + Инфраструктура
    - Прибыль = Выручка - Расходы
    - ROI = (Прибыль / Расходы) × 100%

    ### Управление моделью
    - Все параметры настраиваются в разделе Parameter Management
    - Сравнение сценариев доступно в разделе Scenario Analysis
    - Поддерживается создание и сохранение пользовательских сценариев
    """)

if __name__ == "__main__":
    documentation_page()